#!/usr/bin/env node
/**
 * Generate docs/coverage/catalog-coverage.md from packages/shared/methods/registry.json.
 * Task 25 gate: assert total tool count meets minimum threshold.
 */
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

import { findTerravoltRepoRoot } from "./lib/repo_root.mjs";

const root = findTerravoltRepoRoot(import.meta.url);
const regPath = join(root, "packages", "shared", "methods", "registry.json");
const outDir = join(root, "docs", "coverage");
const outPath = join(outDir, "catalog-coverage.md");

const MIN_TOOLS = Number.parseInt(process.env.TERRAVOLT_MIN_CATALOG_TOOLS ?? "200", 10);
const reg = JSON.parse(readFileSync(regPath, "utf8"));
const methods = reg.methods ?? [];

const byCategory = {};
for (const m of methods) {
  const cat = String(m.category ?? m.method?.split(".")[0] ?? "unknown");
  if (!byCategory[cat]) byCategory[cat] = [];
  byCategory[cat].push(m.method);
}

const sortedCats = Object.keys(byCategory).sort();
const headless = methods.filter((m) => m.headlessFallback).length;
const editorOnly = methods.filter((m) => m.requiresEditor).length;

const lines = [
  "# Catalog coverage report",
  "",
  "Auto-generated by `npm run coverage:report` — do not edit by hand.",
  "",
  `**Generated:** ${new Date().toISOString()}`,
  "",
  "| Metric | Value |",
  "| ------ | ----- |",
  `| \`catalog_version\` | **${reg.catalog_version}** |`,
  `| Total methods | **${methods.length}** |`,
  `| \`headlessFallback\` | **${headless}** |`,
  `| \`requiresEditor\` | **${editorOnly}** |`,
  "",
  "## By category",
  "",
  "| Category | Count | Methods |",
  "| -------- | ----: | ------- |",
];

for (const cat of sortedCats) {
  const list = byCategory[cat].sort();
  lines.push(
    `| \`${cat}\` | ${list.length} | ${list.slice(0, 4).join(", ")}${list.length > 4 ? ", …" : ""} |`,
  );
}

lines.push(
  "",
  "## Gate",
  "",
  `- Minimum tools: **${MIN_TOOLS}**`,
  `- Status: **${methods.length >= MIN_TOOLS ? "PASS" : "FAIL"}**`,
  "",
);

mkdirSync(outDir, { recursive: true });
writeFileSync(outPath, lines.join("\n"), "utf8");

process.stdout.write(`coverage:report: ${methods.length} tools → ${outPath}\n`);

if (methods.length < MIN_TOOLS) {
  process.stderr.write(`coverage:report: FAIL (${methods.length} < ${MIN_TOOLS})\n`);
  process.exit(1);
}
